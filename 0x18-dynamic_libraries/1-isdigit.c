/**
 * _isdigit - Checks if c is a digit
 * @c: Parameter to check if it is digit or not
 * Description: Checks if c is a digit
 * Return: int
 */
int _isdigit(int c)
{
	if (c >= 48 && c <= 57)
		return (1);
	else
		return (0);
}
