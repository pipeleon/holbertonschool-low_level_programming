#include <stdio.h>
/* Task 8 0x01 Variables, if, else, while  */

/**
 * main - Print number base 16
 * Description: Print numbers base 16
 * Return: 0
 */
int main(void)
{
	int a;
	int b;

	for (a = 48; a <= 57; a++)
		putchar(a);

	for (b = 97; b <= 102; b++)
		putchar(b);

	putchar('\n');

	return (0);
}
